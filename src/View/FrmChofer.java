    private boolean isValidCapacity(String capacity) {
    try {
        int value = Integer.parseInt(capacity);
        if (value < Integer.MIN_VALUE || value > Integer.MAX_VALUE) {
            JOptionPane.showMessageDialog(null, "Estas loco?, ese numero es gigantezco ", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "La capacidad debe ser un número", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
}


    
