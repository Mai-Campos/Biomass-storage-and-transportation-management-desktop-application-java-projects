    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
          DefaultTableModel model = (DefaultTableModel)tblVehicles.getModel();
        ArrayList<Vehicles> arrayVehicles = new ArrayList<>();

        for (int i=0; i<model.getRowCount(); i++) {                
            switch (model.getValueAt(i, 3).toString()) {
                case "Carro":
                    arrayVehicles.add(new Car(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), Integer.parseInt(model.getValueAt(i, 2).toString())));
                    break;
                case "Camion":
                    arrayVehicles.add(new Truck(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), Integer.parseInt(model.getValueAt(i, 2).toString())));
                    break;
                case "Motor":
                     
                    arrayVehicles.add(new Bike(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), Integer.parseInt(model.getValueAt(i, 2).toString())));
                    break;
                    
                    
                
            }
            
        }
        
        CtrlVehicles ctrl = new CtrlVehicles();
        boolean isSaved = ctrl.saveVehicles(arrayVehicles);
        
        if ( isSaved ) {
            JOptionPane.showMessageDialog(null, "Información guardada con éxito", "Garajes", JOptionPane.INFORMATION_MESSAGE);  
            
   
            
            
        } else {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al guardar los datos", "Garajes", JOptionPane.ERROR_MESSAGE);                        
        }         // TODO add your handling code here:
        change = false;
    }//GEN-LAST:event_btnSaveMouseClicked

